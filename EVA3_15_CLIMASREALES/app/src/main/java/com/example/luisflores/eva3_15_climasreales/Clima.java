package com.example.luisflores.eva3_15_climasreales;

public class Clima {

    String sNombreCiudad;
    int iTemperatura;
    String sDescripcion;
    int iImagen;

    public Clima(String sNombreCiudad, int iTemperatura, String sDescripcion, int iImagen) {
        this.sNombreCiudad = sNombreCiudad;
        this.iTemperatura = iTemperatura;
        this.sDescripcion = sDescripcion;
        this.iImagen = iImagen;
    }
}